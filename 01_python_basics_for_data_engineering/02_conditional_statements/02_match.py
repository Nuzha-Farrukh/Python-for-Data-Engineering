# The match statement is used to perform different actions based on different conditions.
# The match statement selects one of many code blocks to be executed.
# same as switch case in other languages.

day_in_week = 2
match day_in_week:
  case 1:
    print("Monday")
  case 2:
    print("Tuesday")
  case 3:
    print("Wednesday")
  case 4:
    print("Thursday")
  case 5:
    print("Friday")
  case 6:
    print("Saturday")
  case 7:
    print("Sunday")
